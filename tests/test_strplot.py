from dicechanics._strplot import str_plot


def test_strplot(d6):
	d = 2 @ d6
	string = str_plot(d)
	assert (
		string
		== "2 |### 0.03\n3 |###### 0.06\n4 |########## 0.08\n5 |############# 0.11\n6 |################ 0.14\n7 |#################### 0.17\n8 |################ 0.14\n9 |############# 0.11\n10|########## 0.08\n11|###### 0.06\n12|### 0.03\n"  # noqa: E501
	)
